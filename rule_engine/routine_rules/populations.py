# L04 — 초보/중급/고급 정의
# ----------------------------------------------------------------------------
# rules/L04.yaml | csv 118행
# 출처: Rippetoe (Practical Programming)
# URL : https://startingstrength.com/article/training_age  ★★★
# ============================================================================
L04 = Citation(
    rule_id="L04", name="초보/중급/고급 정의",
    source="Rippetoe",
    url="https://startingstrength.com/article/training_age",
    confidence="★★★",
)

def l04_classify_level(training_months: int) -> str:
    """L04: 훈련 개월 → 레벨.

    초보: <6개월 (매 세션 진보)
    중급: 6~24개월 (주간 진보)
    고급: 24+개월 (메조사이클 진보)
    """
    if training_months < 6:
        return "초보"
    if training_months < 24:
        return "중급"
    return "고급"


